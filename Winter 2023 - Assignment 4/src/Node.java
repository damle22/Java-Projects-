public class Node {

	private Book b;
	private Node next;
	
	
/**
 * This is the get book method.
 * @return the book.
 */
	public Book getB() {
		return b;
	}
/**
 * This is the set book method.
 * @param b is the book.
 */
	public void setB(Book b) {
		this.b = b;
	}
/**
 * This is the get next method.
 * @return the next node.
 */
	public Node getNext() {
		return next;
	}
/**
 * This is the set next node method.
 * @param next is the next node.
 */
	public void setNext(Node next) {
		this.next = next;
	}
	
}
